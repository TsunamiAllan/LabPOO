    private void jButtonComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonComprarActionPerformed
        Cliente c = new Cliente();
        c.setNome(this.jTextFieldNomePassageiro.getText());
        c.setTelefone(this.jTextFieldTelefone.getText());
        c.setPagamento(this.jComboBoxPagamento.getSelectedItem().toString());
        boolean teste = onibus.addPassageiro(this.jComboBoxAssentos.getSelectedIndex(), c);
        if(teste)
        {
            JOptionPane.showMessageDialog(this, "Passagem Comprada com Sucesso!");
            ObjectMapper mapper = new ObjectMapper();
            try {
                this.arquivo.setConteudo(mapper.writeValueAsString(this.onibus));
                this.arquivo.salvar();                
            } catch (JsonProcessingException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.jTextFieldNomePassageiro.setText("");
            this.jTextFieldTelefone.setText("");
            this.jComboBoxAssentos.setSelectedIndex(this.jComboBoxAssentos.getSelectedIndex()+1);
            this.jComboBoxPagamento.setSelectedIndex(0);
        }else
        {
             JOptionPane.showMessageDialog(this, "Não foi possível comprar a passagem");
        }
    }//GEN-LAST:event_jButtonComprarActionPerformed

