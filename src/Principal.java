    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int input = JOptionPane.showConfirmDialog(null,
                                      "Tem certeza que deseja apagar todos os passageiros?"
                                              + "\nEssa operação é irreversível!",
                                      "Começar novo ônibus",
                                      JOptionPane.YES_NO_OPTION,
                                      JOptionPane.ERROR_MESSAGE);
        if (input == 0){
            this.onibus = new Onibus();
            try {
                ObjectMapper mapper = new ObjectMapper();
                this.arquivo.setConteudo(mapper.writeValueAsString(this.onibus));
                this.arquivo.salvar();
                JOptionPane.showMessageDialog(null, "Novo Ônibus criado com sucesso!");
            } catch (JsonProcessingException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

